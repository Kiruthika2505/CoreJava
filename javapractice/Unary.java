class Unary
{
	public static void main(String[] args)
	{

		int a=6;			
		System.out.println("A Value:  "+a);
		System.out.println("PreIncrement A Value"+ (++a));
		System.out.println("PostIncrement:  "+ (a++));
		System.out.println("After Post Increment - A Value:  "+ a);

		int b =3;
		System.out.println("B Value:  "+b);
		System.out.println("PreDecrement B Value:   "+ (--b));
		System.out.println("PostDecrement B value:  "+ (b--));
		System.out.println("After Post Decrement - B Value:  "+b);
	}
}
	