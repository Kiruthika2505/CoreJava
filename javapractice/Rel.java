class Rel
{
	public static void main(String args[])
	{
		int a,b;
		a=5;
		b=3;
		
		System.out.println("Check A is less than B:"+(a<b));
		System.out.println("Check A is less than Equal to B:"+(a<=b));
		System.out.println("Check A is Greater than B:"+(a>b));
		System.out.println("Check A is Greater than Equal to B:"+(a>=b));
		System.out.println("Check A is Equal to B:"+(a==b));
		System.out.println("Check A is Not Equal to B:"+(a!=b));
	}
}