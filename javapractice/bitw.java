class bitw
{
	public static void main(String args[])
	{
		int a,b;
		a=2;
		b=5;
	
		System.out.println("A&b: "+(a&b));
		System.out.println("A|b: "+(a|b));
		System.out.println("A|b: "+(a^b));
		System.out.println(~a); // Adding 1 to the value and represent it as minus
		System.out.println(~b); // Subtracting 1 from the value and represent it as positive
	}
}

	


































